package com.dong.demo;

public class SpELMath {

	private float addition;
	private float multiplication;
	private float division;
	private float complementation;
	private float involution;
	
	public float getAddition() {
		return addition;
	}
	public void setAddition(float addition) {
		this.addition = addition;
	}
	public float getMultiplication() {
		return multiplication;
	}
	public void setMultiplication(float multiplication) {
		this.multiplication = multiplication;
	}
	public float getDivision() {
		return division;
	}
	public void setDivision(float division) {
		this.division = division;
	}
	public float getComplementation() {
		return complementation;
	}
	public void setComplementation(float complementation) {
		this.complementation = complementation;
	}
	public float getInvolution() {
		return involution;
	}
	public void setInvolution(float involution) {
		this.involution = involution;
	}

}
